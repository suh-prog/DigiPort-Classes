//Crie uma variável com a string "Javascript é incrível!"
let minhaString = "Javascript é incrível!"

//Acesse e imprima a quinta letra da string
console.log(minhaString[4])

//Use charAt() para acessar o mesmo caractere
console.log(minhaString.charAt(4))

//Substitua a palavra ïncrivel" por "poderoso"
console.log(minhaString.replace("incrível", "poderoso"))

//Imprime o número de caracteres da string
console.log(minhaString.length)

//Use slice() para extrair a palavra "Javascript"
console.log(minhaString.slice(0, 10))

//Use concat() para adicionar "Vamos aprender!" ao final da string
console.log(minhaString.concat(" Vamos aprender!"))

