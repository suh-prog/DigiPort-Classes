/* Faça um script que leia três números inteiros, em
seguida mostre o maior e o menor deles. */

// Solicita 3 números inteiros
let num1 = 42;
let num2 = 17;
let num3 = 63;

// Calcula maior e menor
let maior = Math.max(num1, num2, num3);
let menor = Math.min(num1, num2, num3);

// Exibe resultados
console.log("O maior número é: " , maior);
console.log("O menor número é: " , menor);
